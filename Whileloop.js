//syntax for while loop
/* 

while(condition)
  {
    conditional code(statements);
  }

  */

  // Do while syntax
  /* 

  do {

  } while(condition)

  */

  let counter = 31;

  while (counter <= 30){
    console.log('Counter value is: '+counter);
    counter++
  }

  counter = 31;

  do {
    console.log('Counter value is: '+counter);
  } while (counter <= 30);